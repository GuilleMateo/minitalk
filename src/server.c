#include "minitalk.h"

void	recu_signal(int sig)
{
	static int	bit;
	static int	i;

	if (sig == SIGUSR1)
		i |= (0x01 << bit);
	bit++;
	if (bit == 8)
	{
		ft_printf("%c", i);
		bit = 0;
		i = 0;
	}
}

int	main(void)
{
	ft_printf("PID DU SERVEUR = %d\n\n", getpid());
	while (1)
	{
		signal(SIGUSR1, recu_signal);
		signal(SIGUSR2, recu_signal);
	}
	while (1)
		pause();
	return (0);
}
